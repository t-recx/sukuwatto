def translate language
    json = JSON.parse(File.read("src/assets/i18n/#{language}.json"))

    json.filter { |k, v| k == v }.each do |_, k|
        json[k] = translate_value(k)
    end

    opts = {
        array_nl: "\n",
        object_nl: "\n",
        indent: '  ',
        space_before: ' ',
        space: ' '
    }
    puts JSON.generate(json, opts)
end

def generate_subs language
    json = JSON.parse(File.read("src/assets/i18n/#{language}.json"))

    puts json.filter { |k, v| k == v }
        .map { |k, v| v }
        .flat_map { |x| x.split(',') }
        .map { |x| x.strip }
        .sort_by { |x| -x.length }
        .uniq
        .map { |x| "    .gsub('#{x}', '#{x}')" }
end

